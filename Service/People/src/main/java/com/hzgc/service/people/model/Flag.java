package com.hzgc.service.people.model;

public class Flag {
    private Long id;

    private Long flagid;

    private Integer flag;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getFlagid() {
        return flagid;
    }

    public void setFlagid(Long flagid) {
        this.flagid = flagid;
    }

    public Integer getFlag() {
        return flag;
    }

    public void setFlag(Integer flag) {
        this.flag = flag;
    }
}